#include "win-time.h"

BOOL _cdk_localtime(const time_t* t, struct tm* r) {

	_tzset();
	errno_t err = localtime_s(r, t);

	switch (err) {
	case 0:
		return TRUE;
	default:
		return FALSE;
	}
}

void _cdk_sleep(const DWORD ms) {
	Sleep(ms);
}